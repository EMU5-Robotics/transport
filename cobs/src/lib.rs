#[derive(Debug, PartialEq, Eq, Clone)]
pub enum Error {
	OutputBufferFull,
	UnexpectedEndOfStream,
	ZeroByteFound,
}

// ffs rustdocs
// impl core::error::Error for Error {}

impl core::fmt::Display for Error {
	fn fmt(&self, f: &mut core::fmt::Formatter) -> core::fmt::Result {
		match self {
			Self::OutputBufferFull => write!(f, "Output buffer is too small"),
			Self::UnexpectedEndOfStream => write!(f, "Unexpected end of encoded data"),
			Self::ZeroByteFound => write!(f, "Zero byte found in stream of encoded data"),
		}
	}
}

pub type Result<T> = core::result::Result<T, Error>;

pub const fn max_encode_size(input_size: usize) -> usize {
	match input_size {
		0 => 1,
		n => n + ((n + 254) / 254),
	}
}

pub const fn max_decode_size(input_size: usize) -> usize {
	match input_size {
		n @ 0..=1 => n,
		n => n - 1,
	}
}

pub fn encode<'a>(in_buf: &[u8], out_buf: &'a mut [u8]) -> Result<&'a [u8]> {
	let mut out_i = 1; // index of the output
	let mut code_i = 0; // index of the code

	// check we have enough space for a single byte output
	if out_buf.is_empty() {
		return Err(Error::OutputBufferFull);
	}

	for &b in in_buf {
		// if block is complete then write code and restart
		if out_i - code_i >= 0xFF {
			out_buf[code_i] = 0xFF; // write code
			code_i = out_i; // update code i to cursor
			out_i += 1; // increase cursor
		}

		// check we aren't going to overrun our buffer
		if out_i >= out_buf.len() {
			return Err(Error::OutputBufferFull);
		}

		// if byte == 0, then write our code, otherwise write byte
		if b == 0 {
			// set the code value to the offset
			out_buf[code_i] = (out_i - code_i) as u8;
			code_i = out_i; // new code is written next time
			out_i += 1;
		} else {
			out_buf[out_i] = b;
			out_i += 1;
		}
	}

	// set our final code
	out_buf[code_i] = (out_i - code_i) as u8;

	Ok(&out_buf[..out_i])
}

pub fn decode<'a>(in_buf: &[u8], out_buf: &'a mut [u8]) -> Result<&'a [u8]> {
	let mut out_i = 0; // index of the output
	let mut code_i = 0; // index of the code

	// return nothing if input is empty
	if in_buf.is_empty() {
		return Ok(&out_buf[..0]);
	}

	// loop until we have no more input
	loop {
		let code = in_buf[code_i];
		// if we found 0 then error
		if code == 0 {
			return Err(Error::ZeroByteFound);
		}
		// for each input up to this code, output
		for in_i in (code_i + 1)..(code_i + code as usize) {
			// there should be more bytes
			if in_i >= in_buf.len() {
				return Err(Error::UnexpectedEndOfStream);
			}

			// read byte
			let b = in_buf[in_i];

			if b == 0 {
				return Err(Error::ZeroByteFound);
			}
			if out_i >= out_buf.len() {
				return Err(Error::OutputBufferFull);
			}

			out_buf[out_i] = b;
			out_i += 1;
		}

		// update code_i to our next code
		code_i += code as usize;

		// if code index == msg len, finished
		if code_i >= in_buf.len() {
			break;
		}

		// output at end of coded part
		if code != 0xFF {
			if out_i >= out_buf.len() {
				return Err(Error::OutputBufferFull);
			}
			out_buf[out_i] = 0;
			out_i += 1;
		}
	}

	Ok(&out_buf[..out_i])
}

#[cfg(feature = "alloc")]
pub fn encode_to_vec(in_buf: &[u8]) -> Result<Vec<u8>> {
	let mut vec = Vec::with_capacity(max_encode_size(in_buf.len()));
	// probably safe idk
	unsafe {
		vec.set_len(vec.capacity());
		let len = encode(in_buf, vec.as_mut_slice())?.len();
		vec.set_len(len);
	}
	Ok(vec)
}

#[cfg(feature = "alloc")]
pub fn decode_to_vec(in_buf: &[u8]) -> Result<Vec<u8>> {
	let mut vec = Vec::with_capacity(max_decode_size(in_buf.len()));
	// probably safe idk
	unsafe {
		vec.set_len(vec.capacity());
		let len = decode(in_buf, vec.as_mut_slice())?.len();
		vec.set_len(len);
	}
	Ok(vec)
}

